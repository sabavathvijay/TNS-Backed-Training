// We can achieve Abstraction using Interfaces instead class 
// The Interface is a Complete Abstract "class "  that is used for Empty body Methods 
// and it is a Blue print of Class
/*
 All methods in Interface are Implicitly "public and Abstract before Java-8" 
 
  ==> And after the Version java-8 and java-9 the it Introduced 
  1. Default methods -(uses "default" keyword )
  2. Static method -(uses "static" keyword )
  
  -> And these are Concrete methods 
  
  ==> No Need to use "abstract" keyword for Abstract Methods
 
 */

